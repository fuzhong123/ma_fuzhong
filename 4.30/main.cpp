#include <iostream>

using namespace std;

int main()
{
    double r=0,a=3.14159;
    cout<<"Enter the radius of the circle :";
    cin>>r;
    cout<<"The diameter of the circle is :"<<2*r<<endl;
    cout<<"The diameter of the Perimeter is :"<<2*a*r<<endl;
    cout<<"The area of the circle is :"<<a*r*r<<endl;
    return 0;
}
